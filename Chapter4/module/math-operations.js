// export the add functionnality
export const add = (a, b) => {
    return Number(a) + Number(b);
}

// export the multiply functionnality
export const multiply = (a, b) => {
return Number(a) * Number(b);
}

// export the devide functionnality
export const devide = (a, b) => {
return Number(a) / Number(b);
}